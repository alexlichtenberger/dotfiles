-- [[ Settings ]]
-- Set space as leader
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- enable nerd font
vi.g.have_nerd_font = true

-- enable folding, but not by default
vim.opt.foldenable = false
vim.opt.foldmethod = "indent"

-- Relative numbers
vim.opt.number = true
vim.opt.relativenumber = true

-- spaces, not tabs
vim.opt.expandtab = true
vim.opt.shiftwidth = 2

-- Enable mouse, it's not 1476
vim.opt.mouse = "a"

-- sync clipboard with OS
vim.opt.clipboard = "unnamedplus"

-- Smarter searching (re: case sensitivity)
vim.opt.ignorecase = true
vim.opt.smartcase = true

--always show the sign column
vim.opt.signcolumn = "yes"

-- decrease some delays
vim.opt.timeoutlen = 300
vim.opt.updatetime = 250

-- control default splitting behavior
vim.opt.splitright = true
vim.opt.splitbelow = true

-- Show special tabs and trailing spaces visually so you don't accidentally use them
vim.opt.list = true
vim.opt.listchars = { tab = "» ", trail = "·", nbsp = "␣" }

-- show line that cursor is on
vim.opt.cursorline = true

-- scroll offset
vim.opt.scrolloff = 10

-- [[ Keymaps ]]

-- better search highlighting
vim.opt.hlsearch = true
vim.keymap.set("n", "<esc>", "<cmd>nohlsearch<cr>")

-- [F]ile
